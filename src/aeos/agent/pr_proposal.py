def generate_pr_proposal(
    project_name: str,
    registry_path: Path | None = None,
) -> PRProposal:
    """Generate a deterministic PR proposal for a registered project.

    Pure read — never writes files, never modifies the registry,
    never contacts any network endpoint. No LLM. No .env access.

    Raises FileNotFoundError if the registry does not exist.
    Raises ValueError if the project is not found in the registry.
    """
    from aeos.memory.timeline import load_project_records
    from aeos.project.registry import DEFAULT_REGISTRY, load_registry

    reg_path = registry_path if registry_path is not None else DEFAULT_REGISTRY

    if not reg_path.exists():
        raise FileNotFoundError(
            f"Registry not found: {reg_path}. Run 'aeos workspace init' to initialise."
        )

    registry = load_registry(reg_path)
    proj = next((p for p in registry.projects if p.name == project_name), None)
    if proj is None:
        known = [p.name for p in registry.projects] or ["(none)"]
        raise ValueError(
            f"Project '{project_name}' not found in registry. Known projects: {known}"
        )

    # Load MemoryRecords — graceful if memory_dir absent or empty
    record_count = 0
    critical = 0
    important = 0
    manual = 0
    generated = 0
    control_level = "unknown"
    providers: list[str] = []
    latest_record_id: str | None = None

    try:
        records = load_project_records(proj.memory_dir, project_name)
        record_count = len(records)
        if records:
            latest = records[-1]
            fs = latest.findings_summary
            critical = fs.get("critical", 0)
            important = fs.get("important", 0)
            manual = fs.get("manual", 0)
            generated = fs.get("generated", 0)
            control_level = latest.control_level
            providers = latest.providers
            latest_record_id = latest.record_id
    except (FileNotFoundError, ValueError):
        pass

    production_ready = critical == 0 and generated == 0

    title = _build_title(
        project_name, critical, important, generated, control_level, production_ready
    )
    objective = _build_objective(critical, important, generated, production_ready)
    why_now = _build_why_now(critical, important, generated, manual, production_ready)
    scope = _build_scope(
        critical, important, generated, manual, providers, production_ready
    )
    impl_steps = _build_implementation_steps(
        critical, important, generated, manual, providers, project_name
    )
    val_cmds = _build_validation_commands(project_name, proj.memory_dir)
    checklist = _build_approval_checklist(critical, important, generated, manual)
    risks = _build_risks(critical, important, generated, control_level, providers)
    rollback = _build_rollback(generated, providers)
    likely_files = _build_likely_files(providers, proj.evidence_dir)

    evidence: dict[str, object] = {
        "record_count": record_count,
        "latest_record_id": latest_record_id,
        "evidence_dir": str(proj.evidence_dir) if proj.evidence_dir else None,
        "production_ready": production_ready,
        "critical": critical,
        "important": important,
        "manual": manual,
        "generated_sql_blocks": generated,
        "control_level": control_level,
        "providers": providers,
        "registry_path": str(reg_path),
    }

    return PRProposal(
        project_name=project_name,
        generated_at=datetime.now(tz=UTC).strftime("%Y-%m-%dT%H:%M:%SZ"),
        agent_mode=PR_PROPOSAL_MODE,
        title=title,
        objective=objective,
        why_now=why_now,
        recommended_scope=scope,
        out_of_scope=list(_OUT_OF_SCOPE_ALWAYS),
        likely_files=likely_files,
        safety_constraints=list(_SAFETY_CONSTRAINTS),
        implementation_steps=impl_steps,
        validation_commands=val_cmds,
        approval_checklist=checklist,
        risks=risks,
        rollback_notes=rollback,
        evidence_references=evidence,
    )


# ---------------------------------------------------------------------------
# Terminal summary renderer
# ---------------------------------------------------------------------------


def render_pr_proposal_summary(proposal: PRProposal) -> str:
    lines: list[str] = []
    a = lines.append

    a("AEOS PR Proposal")
    a("")
    a(f"Mode:              {proposal.agent_mode}")
    a(f"Project:           {proposal.project_name}")
    a(f"Generated:         {proposal.generated_at}")
    ev = proposal.evidence_references
    a(f"Records:           {ev.get('record_count', 0)}")
    crit = ev.get("critical", 0)
    imp = ev.get("important", 0)
    gen = ev.get("generated_sql_blocks", 0)
    if crit or imp or gen:
        a(f"Findings:          critical={crit}  important={imp}  sql_blocks={gen}")
    a("")
    a(f"  Title:  {proposal.title}")
    a("")

    if proposal.why_now:
        a("  Why now:")
        for r in proposal.why_now:
            a(f"    ! {r}")
        a("")

    if proposal.recommended_scope:
        a("  Recommended scope:")
        for i, s in enumerate(proposal.recommended_scope[:4], 1):
            a(f"    {i}. {s}")
        if len(proposal.recommended_scope) > 4:
            remaining = len(proposal.recommended_scope) - 4
            a(f"    ... and {remaining} more — see --output for full proposal")
        a("")

    if proposal.validation_commands:
        a("  Validation commands:")
        for cmd in proposal.validation_commands[:3]:
            a(f"    → {cmd}")
        a("")

    a("  read_only: true  ·  applied: false  ·  human validation required")
    a(f"  {proposal.final_statement}")

    return "\n".join(lines)


